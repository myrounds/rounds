<template>
    <div class="my-rounds">
        <div class="mui--appbar-height"></div>
        <div class="mui-container-fluid">

            <br>
            <spinner v-bind:loading="loading"></spinner>

            <div v-for="task in tasks" class='group-hold'>
                <div class="mui-panel" v-bind:tasks="JSON.stringify(task.items)" @click="showDetails">
                    <div>
                         <span class="group-name"> <b> {{ task.name }} </b> &bull; {{ task.items.length }} items </span>
                         <span class="time">{{ task.time }}</span>
                    </div>
                    <div class="mui--divider-top group-address">
                        {{ task.address }}
                    </div>
                    <div id='group-tasks'></div>
                </div>
               <!--  <div style="width: 100%; text-align: center; margin-top: -20px; color: #aaa;">v</div> -->
            </div>

            <div class="mui-panel">
                <i class="fas fa-check-circle"></i> All Rounded up for Today
            </div>
        </div>
    </div>
</template>

<script>
    import axios from 'axios';
    import DateTime from '../../helpers/datetime';
    import Spinner from '../../components/spinner';
    export default {
        components: {Spinner},
        data() {
            return {
                loading: false,
                tasks: null,
                error: null,
                day: null
            };
        },
        created() {
            this.fetchData();
        },
        methods: {
            fetchData() {
                this.error = this.groups = [];
                this.loading = true;
                const Authorization = 'Bearer ' + JSON.parse(localStorage.getItem('user')).token;

                this.day = DateTime.getCurrentDay();

                axios
                    .get('/api/tasks/search', {
                        headers: { Authorization },
                        params: {
                            s_day: this.day,
                            e_day: this.day,
                        }
                    })
                    .then(response => {
                        const payload = response.data;
                        this.tasks = payload.data;
                        this.loading = false;
                    })
                    .catch(error => {
                        const payload = error.response.data;
                        this.loading = false;
                        this.$msg(payload.message);
                    });
            },
            showDetails(event) {
                var modalEl = document.createElement('div');
                modalEl.style.width = '90%';
                modalEl.style.minHeight = '300px';
                modalEl.style.margin = '100px auto';
                modalEl.style.backgroundColor = '#fff';
                modalEl.style.padding = '20px';

                const tasks = JSON.parse(event.target.getAttribute('tasks') || event.target.parentNode.getAttribute('tasks'));

                modalEl.innerHTML += `
                <legend>Task Items</legend>`;
                tasks.forEach(task => {
                    modalEl.innerHTML += `
                    <div class="mui--divider-top">
                        <div>
                            <strong>
                                ${task.name} | ${task.quantity} | ${task.complete ? 'completed' : 'pending'}
                            </strong>
                            <div>
                                ${task.notes}
                            </div>
                        </div>
                    </div>`;
                });

                // show modal
                mui.overlay('on', modalEl);
            }
        }
    }
</script>